//con el switch tambien vas a validar ciertas cosas en cada uno de los casos
//va a funcionar por casos
var numero = 1;
//      (true)
switch (numero) {
  case 1:
    console.log("Soy uno");
    break;
  case 10:
    console.log("Soy un diez");
    break;
  case 100:
    console.log("Soy un cien");
    break;
  default:
    console.log("no soy nada");
}
